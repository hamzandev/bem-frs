        <div class="space-y-5 text-justify">
            <h2 class="text-3xl text-black font-extrabold mb-5">Sejarah FRS</h2>
            <p class="leading-relaxed">
                Pada tahun 2015, Universitas Teknologi Sumbawa mengeluarkan kebijakan berupa pemekaran Fakultas Teknik
                menjadi dua fakultas yaitu Fakultas Teknologi Informatika (FTI) dan Fakultas Teknologi Pertambangan
                (FTT). Fakultas Teknologi Informatika menjadi fakultas independen dengan 3 program studi (Prodi), yaitu
                Prodi Teknik Informatika, Prodi Teknik Elektro, dan Prodi Teknik Sipil. Sedangkan Fakultas Teknologi
                Pertambangan dengan 3 Prodi lainnya yaitu Prodi Teknik Mesin, Prodi Teknik Metalurgi dan Prodi Teknik
                Industri.
            </p>
            <p class="leading-relaxed">
                Pada tahun 2018, kedua fakultas tersebut dilebur kembali menjadi Fakultas Teknik agar serumpun dan lebih
                efisien. Namun, pada tahun 2021 karena semakin banyaknya program studi di UTS khususnya di Fakultas
                Teknik, Fakultas Teknik dimekarkan menjadi FTLM dan FRS dengan tujuan untuk menjaga efektivitas
                administrasi dan distribusi pekerjaan di fakultas (simpelnya mengurangi beban Fakultas).
            </p>
            <p class="leading-relaxed">
                Fakultas Rekayasa Sistem merupakan satu dari delapan fakultas di Universitas Teknologi Sumbawa yang
                berisikan Program Studi Teknik Mesin, Program Studi Teknik Industri, Program Studi Teknik Informatika,
                Program Studi Teknik Elektro, dan Program Studi Teknik Sistem Energi. Hari jadi FRS tanggal 1 Juli.
            </p>
        </div>
        <div class="space-y-5 text-justify">
            <h2 class="text-3xl text-black font-extrabold mb-5">Pembentukan BEM FRS</h2>
            <p class="leading-relaxed">
                Guna untuk meningkatkan produktivitas mahasiswa, dibutuhkan ormawa sebagai wadah kegiatan mahasiswa di
                tingkat fakultas. Muncullah gagasan dari seluruh Kahim HMJ FRS untuk melaksanakan kongres. Maka
                tercetuslah rencana untuk pelaksanaan kongres FRS yang dilaksanakan pertama kali pada 18 Desember 2021.
            </p>
            <p class="leading-relaxed">
                Lalu pada tanggal 20 Januari 2022 terbentuklah Dewan Perwakilan Mahasiswa (DPM) dan Badan Eksekutif
                Mahasiswa (BEM) FRS. Tanggal 20 Januari menjadi hari jadi DPM FRS. (Hari jadi BEM FRS belum diketahui
                apakah disamakan dengan DPM atau berbeda).
            </p>
